function handlefilter() {
    var sortArr = [];
    var selected = document.getElementById("sorting").value;

    if (selected == "Low") {
        sortArr = menProd.sort((a, b) => {
            return parseFloat(a.price) - parseFloat(b.price);
        });
    }
    else if (selected == "High") {
        sortArr = menProd.sort((a, b) => {
            return parseFloat(b.price) - parseFloat(a.price);
        });
    }
    else if (selected == "") {
        displayMenProd(menProd);
        return;
    }
    displayMenProd(sortArr);


    // if (selected == "Percent" || selected == "New") {
    //     empty();
    // }
}

function handleBrand() {
    var sel = document.getElementById("brand").value;

    var filterArr = []
    filterArr = menProd.filter(function (elem) {
        if (sel === "") {
            return 1;
        }
        else {
            return elem.brand == sel;
        }
    });
    displayMenProd(filterArr);

}

function handlecat() {
    var sel = document.getElementById("category").value;

    var catArr = []
    catArr = menProd.filter(function (elem) {
        if (sel === "") {
            return 1;
        }
        else if (sel == "sandals" || sel == "sneakers" || sel == "shoes") {
            return elem.type == sel;
        }
    });
    displayMenProd(catArr);

}



function handleRate() {
    var sel = document.getElementById("rate").value;

    var rateArr = [];
    rateArr = menProd.filter(function (elem) {
        var ans = parseInt(elem.rating);
        if (sel === "") {
            return 1;
        }

        else if (sel == "s_1") {
            return (ans == 1);
        }
        else if (sel == "s_2") {
            return (ans == 2);
        }
        else if (sel == "s_3") {
            return (ans == 3);
        }
        else if (sel === "s_4") {
            return (ans == 4);
        }
        else if (sel === "s_5") {
            return (ans === 5);
        }
    });
    displayMenProd(rateArr);

}


function handlePrice() {
    var sel = document.getElementById("priceE").value;

    var filArr = []
    filArr = menProd.filter(function (elem) {
        var ans = parseFloat(elem.price);
        if (sel === "") {
            return 1;
        }
        else if (sel == "Under") {
            return (ans < 30);
        }
        else if (sel == "Between_30") {
            return (ans > 30 && ans < 50);
        }
        else if (sel == "Between_50") {
            return (ans > 50 && ans < 75);
        }
        else if (sel === "above") {
            return (ans > 75);
        }
    });
    displayMenProd(filArr);

}



function displayMenProd(menProd) {
    document.querySelector("#product").textContent = "";
    menProd.map(function (elem) {

        var mast = document.createElement("div");
        mast.setAttribute("class", "mast");


        mast.addEventListener("click", function () {
            productDetails(elem);
        });

        // wishlist

        var i = document.createElement("h3");
        i.innerHTML = "<i class='fa-sharp fa-regular fa-heart'></i>";
        i.setAttribute("class", "i");
        i.addEventListener("click", function () {
            addToWhish(elem);
        });


        //    img section

        var outer = document.createElement("div");
        outer.setAttribute("class", "outer");


        var img1 = document.createElement("img");

        img1.setAttribute("src", elem.img_u1);
        img1.setAttribute("alt", elem.name);


        var inner = document.createElement("div");
        inner.setAttribute("class", "inner");


        var img2 = document.createElement("img");

        img2.setAttribute("src", elem.img_u2);
        img2.setAttribute("alt", elem.name);

        //  name

        var para = document.createElement("div");
        para.setAttribute("class", "para");


        var h4 = document.createElement("h4");
        h4.textContent = elem.brand;


        var p1 = document.createElement("p");
        p1.textContent = elem.name;

        //    prices box

        var box = document.createElement("div");
        box.setAttribute("class", "box1");

        var p2 = document.createElement("p");
        p2.textContent = "$" + elem.price;


        var p7 = document.createElement("p");
        p7.textContent = "$" + elem.MRP;


        // rating

        var imgR = document.createElement("img");

        imgR.setAttribute("src", elem.rate_img);
        imgR.setAttribute("alt", elem.rating);

        inner.append(img2);

        box.append(p2, p7);

        para.append(h4, p1, box, imgR);

        outer.append(img1, inner);

        mast.append(i, outer, para);

        document.querySelector("#product").append(mast);

    });

    var whishArr = JSON.parse(localStorage.getItem("whishProducts")) || [];
    function addToWhish(elem) {
        whishArr.push(elem);
        console.log(whishArr);
        localStorage.setItem("whishProducts", JSON.stringify(whishArr));
    }




    var DetailsArr = [];
    function productDetails(elem) {
        DetailsArr.push(elem);
        window.location.assign("./pruduct_details.html");
        localStorage.setItem("ProductDetails", JSON.stringify(DetailsArr));
    }

}
